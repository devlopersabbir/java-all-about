
public class IfCondition {
    /*
     * Java Condition and if statement
     */
    public static void main(String[] args) {
        // if statement
        // Static value
        if (20 < 30) {
            System.out.println("true :(");
        }
        /*
         * if else with dynamic
         */
        int a = 2;
        int b = 30;
        if (a < b) {
            System.out.println("true");
        } else {
            System.out.println("false");
        }
    }
}
