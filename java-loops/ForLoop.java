public class ForLoop {
    public static void main(String[] args) {
        /*
         * For Loop
         */
        for (int i = 0; i < 10; i++) {
            System.out.println("Number: " + i);
        }

        // everytime increment will be 2 times
        for (int i = 0; i < 10; i = i + 2) {
            System.out.println("Number2: " + i);
        }
    }
}
